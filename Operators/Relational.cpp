#include <iostream>
using namespace std;

//Relational operators

main()
{
	
	int a = 10, b = 5;
	
	//True = 1, False = 0
	
	cout << (a == b) << endl; //True, if equal to.
	cout << (a != b) << endl; //True, if not equal to.
	cout << (a > b) << endl; //True, if a greater than b.
	cout << (a < b) << endl; //True, if a smaller than b.
	cout << (a >= b) << endl; //True, if greater or euqal.
	cout << (a <= b) << endl; //True, if smaller or equal. 
	
	cout << !(a == b) << endl; //Not statement.
	
}

